#!/usr/bin/env python
"""Run the full model comparison on the active dataset and write a Markdown
report to ``docs/benchmarks.md`` (a tracked file, so results are visible on
GitHub).

    python scripts/benchmark.py --label shrunk-philadelphia

The report keeps one **labeled section per dataset** so results from different
slices (e.g. full vs shrunk Philadelphia) coexist and can be compared. Each run
regenerates only its own ``--label`` section and leaves the others untouched.

The section captures the dataset summary, run date, per-model metrics at K=10,
and wall-clock fit+recommend time, so the numbers are reproducible and honest.
"""

import argparse
import os
import re
import sys
import time
from datetime import date
from pathlib import Path

# Single-thread BLAS for stable timing on small problems.
os.environ.setdefault("OPENBLAS_NUM_THREADS", "1")
os.environ.setdefault("OMP_NUM_THREADS", "1")

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

import pandas as pd

from src.recsys.config import settings
from src.recsys.data import load_dataset
from src.recsys.eval import evaluate, temporal_split
from src.recsys.models import (
    ALSRecommender,
    BPRRecommender,
    ContentTwoTowerRecommender,
    PopularityRecommender,
    SASRecRecommender,
    SocialRecommender,
    TextEmbeddingRecommender,
    TwoStageRecommender,
    TwoTowerRecommender,
)

REPORT_PATH = Path(__file__).resolve().parents[1] / "docs" / "benchmarks.md"


def build_models(ds):
    """The comparison set. Two-stage is run with and without the social feature
    so the social lift (or lack of it) is explicit."""
    return {
        "popularity": PopularityRecommender(),
        "als": ALSRecommender(factors=64, iterations=15),
        "bpr": BPRRecommender(factors=64, iterations=80),
        "social": SocialRecommender(social=ds.social),
        "sasrec": SASRecRecommender(dim=64, epochs=15, max_len=50),
        "text_embeddings": TextEmbeddingRecommender(items=ds.items),
        "content_two_tower": ContentTwoTowerRecommender(items=ds.items, dim=64, epochs=10),
        "two_stage_no_social": TwoStageRecommender(
            TwoTowerRecommender(dim=64, epochs=10), candidate_n=200, use_social=False
        ),
        "two_stage_social": TwoStageRecommender(
            TwoTowerRecommender(dim=64, epochs=10),
            candidate_n=200,
            use_social=True,
            social=ds.social,
        ),
    }


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "--label",
        default="shrunk-philadelphia",
        help="Section name in benchmarks.md for this dataset (e.g. full-philadelphia).",
    )
    args = ap.parse_args()

    ds = load_dataset()
    print("DATA:", ds.summary(), flush=True)
    train, test_pos = temporal_split(ds.interactions)
    users = list(test_pos.keys())
    max_k = max(settings.eval_ks)

    rows, times = {}, {}
    t_all = time.time()
    for name, model in build_models(ds).items():
        t = time.time()
        model.fit(train)
        recs = model.recommend(users, k=max_k)
        times[name] = time.time() - t
        rows[name] = evaluate(recs, test_pos, ks=settings.eval_ks, n_items=ds.n_items)
        print(
            f"{name:22s} {times[name]:6.1f}s  "
            f"recall@10={rows[name]['recall@10']:.4f} "
            f"ndcg@10={rows[name]['ndcg@10']:.4f}",
            flush=True,
        )
    total = time.time() - t_all

    df = pd.DataFrame(rows).T
    # Also drop a raw CSV next to the processed data (local only, gitignored).
    processed_dir = settings.paths["processed"]
    processed_dir.mkdir(parents=True, exist_ok=True)
    df.to_csv(processed_dir / f"benchmark_results_{args.label}.csv")

    write_report(df, times, ds, total, args.label)
    print(f"\nTOTAL {total:.1f}s — section '{args.label}' written to {REPORT_PATH}")


PREAMBLE = """# Benchmark results

Auto-generated by `scripts/benchmark.py`. One section per dataset slice — each
run regenerates only its own `--label` section. Reproduce with:

```bash
python scripts/benchmark.py --label shrunk-philadelphia
```

## How to read this"""

HOW_TO_READ = """
- **recall@K / ndcg@K** — did relevant items make the top-K, and are they ranked
  high? NDCG rewards good ordering (ranking quality).
- **hitrate@10** — fraction of users who got at least one relevant item.
- **coverage@10** — share of the catalog that ever appears (diversity /
  anti-popularity-bias).
- Absolute numbers are modest by design: real, sparse data with a temporal split
  is a hard, honest test — not the inflated scores a random split would give.

The two-stage pipeline (retrieval → ranking) is the production pattern; the
`_social` variant adds the friend-graph signal as a ranker feature so its
contribution is measured directly against the identical `_no_social` pipeline.
"""


def _section_markdown(df, times, ds, total, label) -> str:
    cols = ["recall@10", "ndcg@10", "hitrate@10", "recall@20", "coverage@10"]
    cols = [c for c in cols if c in df.columns]

    header = "| model | " + " | ".join(cols) + " | fit+rec (s) |"
    sep = "|" + "---|" * (len(cols) + 2)
    lines = [header, sep]
    for name in df.index:
        cells = " | ".join(f"{df.loc[name, c]:.4f}" for c in cols)
        lines.append(f"| {name} | {cells} | {times[name]:.1f} |")
    table = "\n".join(lines)

    return f"""## {label}

- **Dataset:** {ds.summary()}
- **Split:** temporal (train on past, test on future)
- **Run date:** {date.today().isoformat()}
- **Total run time:** {total:.0f}s (single-thread BLAS, laptop CPU)

{table}
"""


def write_report(df, times, ds, total, label) -> None:
    """Insert/replace one labeled section, preserving all other sections."""
    start = f"<!-- BENCH:{label} -->"
    end = f"<!-- /BENCH:{label} -->"
    section = f"{start}\n{_section_markdown(df, times, ds, total, label)}{end}\n"

    if REPORT_PATH.exists():
        existing = REPORT_PATH.read_text()
    else:
        existing = f"{PREAMBLE}\n{HOW_TO_READ}\n"

    pattern = re.compile(
        re.escape(start) + r".*?" + re.escape(end) + r"\n?", re.DOTALL
    )
    if pattern.search(existing):
        content = pattern.sub(section, existing)
    else:
        # Append the new section before the "How to read" trailer if present,
        # else at the end.
        marker = "## How to read this"
        idx = existing.find(marker)
        if idx != -1:
            content = existing[:idx] + section + "\n" + existing[idx:]
        else:
            content = existing.rstrip() + "\n\n" + section

    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    REPORT_PATH.write_text(content)


if __name__ == "__main__":
    main()
